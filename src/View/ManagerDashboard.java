private void alignTableCenter() {
    DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
    centerRenderer.setHorizontalAlignment(JLabel.CENTER);
    
    tblInventory.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
    tblInventory.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
    tblInventory.getColumnModel().getColumn(5).setCellRenderer(centerRenderer);
    tblInventory.getColumnModel().getColumn(6).setCellRenderer(centerRenderer);

    tblInventory.getColumnModel().getColumn(4).setCellRenderer(new DefaultTableCellRenderer() {
        @Override
        public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            if (value instanceof Number) {
                value = String.format("%.2f", ((Number) value).doubleValue());
            }
            setHorizontalAlignment(JLabel.CENTER);
            return super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        }
    });

    ((DefaultTableCellRenderer)tblInventory.getTableHeader().getDefaultRenderer())
        .setHorizontalAlignment(JLabel.CENTER);

    tblInventory.getColumnModel().getColumn(0).setMaxWidth(60);
    tblInventory.getColumnModel().getColumn(1).setPreferredWidth(220); 
    tblInventory.getColumnModel().getColumn(2).setPreferredWidth(180);
    }
    

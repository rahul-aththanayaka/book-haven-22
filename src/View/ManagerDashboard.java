    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblInventory.getSelectedRow();
    
    if (selectedRow != -1) {
        int bookId = Integer.parseInt(tblInventory.getValueAt(selectedRow, 0).toString());
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to delete this book?", "Confirm Delete", 
                javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            boolean isDeleted = controller.removeBook(bookId);
            
            if (isDeleted) {
                javax.swing.JOptionPane.showMessageDialog(this, "Book deleted successfully!");
                controller.loadInventory();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error deleting the book.");
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a book from the table first.");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblInventory.getSelectedRow();
    
    if (selectedRow != -1) {
        int id = Integer.parseInt(tblInventory.getValueAt(selectedRow, 0).toString());
        String title = tblInventory.getValueAt(selectedRow, 1).toString();
        String author = tblInventory.getValueAt(selectedRow, 2).toString();
        String category = tblInventory.getValueAt(selectedRow, 3).toString();
        double price = Double.parseDouble(tblInventory.getValueAt(selectedRow, 4).toString());
        int qty = Integer.parseInt(tblInventory.getValueAt(selectedRow, 5).toString());

        UpdateBookView updateForm = new UpdateBookView();
        updateForm.setBookDetails(id, title, author, category, price, qty);
        updateForm.setVisible(true);
        updateForm.setDefaultCloseOperation(javax.swing.JFrame.DISPOSE_ON_CLOSE);
        
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a book from the table first!");
    }
    }//GEN-LAST:event_btnUpdateActionPerformed


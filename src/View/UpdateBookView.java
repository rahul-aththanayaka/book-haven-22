    public void setBookDetails(int id, String title, String author, String category, double price, int qty) {
    this.bookId = id;
    txtTitle.setText(title);
    txtAuthor.setText(author);
    cmbCategory.setSelectedItem(category); 
    txtPrice.setText(String.format("%.2f", price));
    txtQty.setText(String.valueOf(qty));
}
    

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
        String title = txtTitle.getText();
        String author = txtAuthor.getText();
        String category = cmbCategory.getSelectedItem().toString(); 
        double price = Double.parseDouble(txtPrice.getText());
        int qty = Integer.parseInt(txtQty.getText());

        Controller.InventoryController controller = new Controller.InventoryController(new View.ManagerDashboard());
        boolean success = controller.updateBookDetails(bookId, title, author, category, price, qty);

        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Book updated successfully!");
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Update failed!");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter valid numbers for Price and Quantity.");
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

